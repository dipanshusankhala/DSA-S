#include<iostream>
using namespace std;

int main()
{
    int i=7;


    cout<< (i++) <<endl;
    // 7   now i=8
    cout<< (++i) <<endl;
    //pehele increment i=9; output 9
    cout<< (i--) <<endl;
    // output=9; now i=8
    cout<< (--i) <<endl;
    // output =7

    return 0;






}



